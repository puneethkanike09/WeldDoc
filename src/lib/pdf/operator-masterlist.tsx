import { Document, Page, View, Text } from "@react-pdf/renderer";
import { COLORS } from "./styles";
import type { OperatorMasterRow } from "@/lib/operator-masterlist";

const COLS: { key: keyof OperatorMasterRow; label: string; w: number }[] = [
  { key: "operatorName", label: "Operator", w: 84 },
  { key: "operatorId", label: "Op. ID", w: 44 },
  { key: "process", label: "Process", w: 40 },
  { key: "weldingType", label: "Type", w: 48 },
  { key: "productType", label: "Product", w: 52 },
  { key: "jointType", label: "Joint", w: 36 },
  { key: "weldingMode", label: "Mode", w: 44 },
  { key: "rangeSummary", label: "Range", w: 120 },
  { key: "status", label: "Status", w: 48 },
  { key: "issued", label: "Issued", w: 52 },
  { key: "expiry", label: "Expiry", w: 52 },
  { key: "revalidation", label: "Reval", w: 32 },
];

const HAIR = 0.5;

export function OperatorMasterListDocument({
  rows,
  orgName,
}: {
  rows: OperatorMasterRow[];
  orgName: string;
}) {
  return (
    <Document title="Operator Master List" author={orgName}>
      <Page
        size="A4"
        orientation="landscape"
        style={{ padding: 22, fontFamily: "Helvetica" }}
      >
        <View
          style={{
            flexDirection: "row",
            justifyContent: "space-between",
            borderBottomWidth: 1.5,
            borderBottomColor: COLORS.onyx,
            paddingBottom: 5,
            marginBottom: 6,
          }}
        >
          <Text style={{ fontFamily: "Helvetica-Bold", fontSize: 12, color: COLORS.onyx }}>
            {orgName}
          </Text>
          <Text style={{ fontFamily: "Helvetica-Bold", fontSize: 10, color: COLORS.onyx }}>
            OPERATOR QUALIFICATION MASTER LIST — ISO 14732:2025
          </Text>
          <Text style={{ fontSize: 8 }}>
            {new Date().toLocaleDateString("en-GB")}
          </Text>
        </View>

        <View style={{ flexDirection: "row", backgroundColor: COLORS.frost }}>
          <Cell w={20} header>
            #
          </Cell>
          {COLS.map((c) => (
            <Cell key={c.key} w={c.w} header>
              {c.label}
            </Cell>
          ))}
        </View>

        {rows.map((r, i) => (
          <View key={i} style={{ flexDirection: "row" }} wrap={false}>
            <Cell w={20}>{String(i + 1)}</Cell>
            {COLS.map((c) => (
              <Cell key={c.key} w={c.w}>
                {String(r[c.key] ?? "—")}
              </Cell>
            ))}
          </View>
        ))}

        <Text style={{ marginTop: 8, fontSize: 7, color: COLORS.steel }}>
          {rows.length} record(s) · Generated by Weld.Doc
        </Text>
      </Page>
    </Document>
  );
}

function Cell({
  w,
  header,
  children,
}: {
  w: number;
  header?: boolean;
  children: React.ReactNode;
}) {
  return (
    <View
      style={{
        width: w,
        padding: 2.5,
        borderWidth: HAIR,
        borderColor: COLORS.silver,
      }}
    >
      <Text
        style={{
          fontSize: 6.5,
          fontFamily: header ? "Helvetica-Bold" : "Helvetica",
          color: header ? COLORS.graphite : COLORS.onyx,
        }}
      >
        {children}
      </Text>
    </View>
  );
}
